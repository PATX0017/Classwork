
public class Project3 {
	 public static void main(String[ ] args) {
		// initialize a counter to 0
		 int counter = 0;

		 // while true = infinite loop
		 while(true) {
		     // increment counter by 1
		     counter++;

		     // break when counter is 5
		     if (counter == 5) {
		         break;
		     }

		     // print if not 3
		     if (counter != 3) {
		         System.out.println(counter);
		     }
	
				}
		 
	 }
}
